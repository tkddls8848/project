function buildNode(spec, raw, index) {
  const legal_basis = raw.bases.map(([lawIndex, article]) => { const law = spec.laws[lawIndex][0].law; return { law, article, text: `${law} ${article}에 따라 ${raw.action}` }; });
  return { id: `P${pad(index + 1)}`, name: raw.name, lane: raw.lane, stage: raw.stage, type: raw.type ?? (/확인|검토|심사|판정|선정|평가|결정/.test(raw.name) ? "gateway" : "task"), status: index < 2 ? "done" : index === 2 ? "current" : raw.type === "gateway" ? "risk" : "waiting", progress: index < 2 ? 100 : index === 2 ? 55 : 0, actor: raw.lane, action: raw.action, output_documents: [`${raw.name} 기록`], deadline: null, confidence: 0.9, legal_basis };
}

